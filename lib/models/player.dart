class Player {
  final int? id;
  final String name;
  final int age;
  final String position;
  final int number;
  final String team;
  final int height;
  final int weight;

  Player({
    required this.id,
    required this.name,
    required this.age,
    required this.position,
    required this.number,
    required this.team,
    required this.height,
    required this.weight,
  });
}
